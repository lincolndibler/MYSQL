#First Query: Find all customers who spent over $50 dollars.
SELECT c.cust_id, c.cust_firstname, c.cust_lastname, SUM(i.item_price * o.quantity) as total_spent
FROM customer c
JOIN orders o ON c.cust_id = o.cust_id
JOIN item i ON o.item_id = i.item_id
GROUP BY c.cust_id
HAVING total_spent > 50
ORDER BY total_spent DESC;

#Second Query Find what is our top 10 most popular items and how many times have they been ordered?
SELECT item.item_name, COUNT(*) AS num_orders, item.item_size
FROM orders
JOIN item ON orders.item_id = item.item_id
GROUP BY orders.item_id
ORDER BY num_orders DESC
LIMIT 10;

#Third Query Similar to above but now we calculate using the quantity of how many were ordered instead of how many times they've been ordered.
SELECT item_name, SUM(orders.quantity) as total_quantity
FROM orders
JOIN item ON orders.item_id = item.item_id
GROUP BY item_name
ORDER BY total_quantity DESC
LIMIT 10;








